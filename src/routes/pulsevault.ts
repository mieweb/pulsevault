// Mirrored in the opt-in `./augment.ts` re-export (for consumers who `import
// "@mieweb/pulsevault/augment"`) and kept here too so this plugin typechecks
// standalone even if the consumer never imports that module.
declare module 'fastify' {
  interface FastifyRequest {
    pulseVault?: { artifactId: string; kind: UploadKind; relatedTo?: string };
  }
}

export type PulseVaultRoutesOptions = {
  storage: PulseVaultStorage;
  maxUploadSize: number;
  allowedExtensions: PulseVaultAllowedExtensions;
  /** Advertised via `GET /capabilities` so the client knows which upload strategy this server expects. */
  uploadUnit: 'segment' | 'merged';
  cache?: PulseVaultCacheOptions;
  authorize?: PulseVaultAuthorize;
  validatePayload?: PulseVaultValidatePayload;
  onUploadComplete?: PulseVaultOnUploadComplete;
  onArtifactEvent?: PulseVaultOnArtifactEvent;
} & FastifyPluginOptions;

// Adds the OpenAPI-flavored fields without pulling `@fastify/swagger` into this
// plugin's type surface; consumers who do register it get the real module
// augmentation and these become native.
type OpenApiRouteSchema = FastifySchema & {
  tags?: string[];
  summary?: string;
  description?: string;
};

const pulseVaultErrorResponse = {
  type: 'object',
  properties: {
    ok: { type: 'boolean' },
    error: { type: 'string' },
  },
  required: ['ok', 'error'],
};

