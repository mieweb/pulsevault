// Fastify doesn't type route params from the schema alone, so `:artifactId` comes through as `unknown`.
type ArtifactIdParams = { artifactId?: unknown };

/** Coerce the raw route param to a string; an invalid UUID is rejected by the core's own check either way. */
function paramToString(value: unknown): string {
  return typeof value === 'string' ? value : '';
}

const pulseVaultRoutes: FastifyPluginAsync<PulseVaultRoutesOptions> = async (fastify, opts) => {
  const {
    storage,
    maxUploadSize,
    allowedExtensions,
    uploadUnit,
    cache,
    authorize,
    validatePayload,
    onUploadComplete,
    onArtifactEvent,
  } = opts;

  // All hook orchestration, tus glue, and artifact GET/DELETE logic lives in
  // the framework-agnostic core (`../core.js`); this file only wires Fastify's
  // routing/schema/lifecycle around it, so behavior can't drift from the
  // `./core` entry point non-Fastify hosts use.
  const core = createPulseVaultCore({
    storage,
    // `fastify.prefix` is `""` when the plugin is mounted at the root.
    basePath: fastify.prefix,
    maxUploadSize,
    uploadUnit,
    allowedExtensions,
    cache,
    authorize,
    validatePayload,
    onUploadComplete,
    onArtifactEvent,
    logger: fastify.log,
  });

  fastify.addContentTypeParser('application/offset+octet-stream', (_request, _payload, done) => {
    done(null);
  });

  const tusMethods = ['POST', 'PATCH', 'HEAD', 'DELETE', 'OPTIONS'] as const;
  const tusHandler = async (request: FastifyRequest, reply: FastifyReply) => {
    // The core writes directly to the raw response (tus dispatch, authorize
    // rejections, error translation) — hijack so Fastify doesn't also try to
    // manage this reply afterward.
    reply.hijack();
    await core.handleTus(request.raw, reply.raw);
  };
  fastify.route({
    method: [...tusMethods],
    url: '/upload',
    schema: tusRouteSchema,
    handler: tusHandler,
  });
  fastify.route({
    method: [...tusMethods],
    url: '/upload/*',
    schema: tusRouteSchema,
    handler: tusHandler,
  });

  fastify.get('/capabilities', { schema: capabilitiesSchema }, async (request, reply) => {
    reply.hijack();
    await core.handleCapabilities(request.raw, reply.raw);
  });

  fastify.delete(
    '/artifacts/:artifactId',
    { schema: artifactDeleteSchema },
    async (request, reply) => {
      const artifactId = paramToString((request.params as ArtifactIdParams)?.artifactId);
      reply.hijack();
      await core.handleArtifactDelete(request.raw, reply.raw, artifactId);
    },
  );

  fastify.get('/artifacts/:artifactId', { schema: artifactGetSchema }, async (request, reply) => {
    const artifactId = paramToString((request.params as ArtifactIdParams)?.artifactId);
    const token = (request.query as { token?: string })?.token;
    reply.hijack();
    await core.handleArtifactGet(request.raw, reply.raw, artifactId, token);
  });
};

export default pulseVaultRoutes;
export { PROTOCOL_VERSION };
