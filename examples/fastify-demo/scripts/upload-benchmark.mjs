#!/usr/bin/env node
// TUS upload benchmark for the pulsevault fastify-demo.
//
// Uploads a video-sized payload the same way a client does (tus create + chunked
// PATCH), times it, verifies the stored size, then deletes it so the demo disk
// doesn't fill. The measured rate is the *client's* uplink, not the server's
// ceiling — run it against http://localhost:3000 to see the server's raw ingest
// (no network in the way), and against prod to see your real connection.
//
// Usage:
//   npm run bench                                  # 300 MB against prod (default)
//   npm run bench -- http://localhost:3000         # 300 MB against a local demo
//   npm run bench -- http://localhost:3000 500 16  # 500 MB, 16 MB chunks
//   npm run bench -- <url> ./myclip.mp4            # a real file instead of generated bytes
//   BENCH_URL=http://localhost:3000 npm run bench  # override the default URL via env
//
// Or without npm: node scripts/upload-benchmark.mjs <url> [sizeMB|file] [chunkMB]

import { readFileSync, statSync } from "node:fs";
import { randomUUID } from "node:crypto";

const DEFAULT_URL = process.env.BENCH_URL ?? "https://pulsevaultdemo.os.mieweb.org";
const [, , baseUrlArg = DEFAULT_URL, sizeOrFile = "300", chunkMBArg = "32"] = process.argv;
const B = baseUrlArg.replace(/\/$/, "");
const CHUNK = Number(chunkMBArg) * 1024 * 1024;

// Payload: a real file if a path was given, else `sizeMB` of generated bytes
// (content is irrelevant to timing — the TUS PATCH body is not compressed).
let buf, label;
if (/^\d+$/.test(sizeOrFile)) {
  buf = Buffer.alloc(Number(sizeOrFile) * 1024 * 1024);
  Buffer.from("\x00\x00\x00\x18ftypmp42\x00\x00\x00\x00mp42isom").copy(buf); // plausible mp4 header
  label = `${sizeOrFile} MB (generated)`;
} else {
  buf = readFileSync(sizeOrFile);
  label = `${sizeOrFile} (${(statSync(sizeOrFile).size / 1048576).toFixed(1)} MB)`;
}

const id = randomUUID();
const b64 = (s) => Buffer.from(s).toString("base64");
const meta = `artifactId ${b64(id)},filename ${b64("benchmark.mp4")},kind ${b64("video")}`;
const mb = buf.length / 1048576;

console.log(`→ ${B}   payload: ${label}   chunk: ${chunkMBArg} MB`);

const t0 = performance.now();
const create = await fetch(`${B}/pulsevault/upload`, {
  method: "POST",
  headers: { "Tus-Resumable": "1.0.0", "Upload-Length": String(buf.length), "Upload-Metadata": meta },
});
if (create.status !== 201) {
  console.error(`create failed: ${create.status} ${await create.text()}`);
  process.exit(1);
}
const loc = create.headers.get("location");

let offset = 0, chunks = 0;
const tUp = performance.now();
while (offset < buf.length) {
  const end = Math.min(offset + CHUNK, buf.length);
  const res = await fetch(`${B}${loc}`, {
    method: "PATCH",
    headers: {
      "Tus-Resumable": "1.0.0",
      "Upload-Offset": String(offset),
      "Content-Type": "application/offset+octet-stream",
    },
    body: buf.subarray(offset, end),
  });
  if (res.status !== 204) {
    console.error(`PATCH @${offset} -> ${res.status} ${await res.text()}`);
    process.exit(1);
  }
  offset = Number(res.headers.get("upload-offset"));
  chunks++;
}
const up = (performance.now() - tUp) / 1000;
const total = (performance.now() - t0) / 1000;

const head = await fetch(`${B}/pulsevault/artifacts/${id}`, { method: "HEAD" });
const stored = head.headers.get("content-length");
await fetch(`${B}/pulsevault/artifacts/${id}`, { method: "DELETE" }); // cleanup

console.log(`✓ uploaded ${mb.toFixed(0)} MB in ${chunks} chunk(s)`);
console.log(`  upload:   ${up.toFixed(1)} s   (${(mb / up).toFixed(1)} MB/s ≈ ${(mb * 8 / up).toFixed(0)} Mbps)`);
console.log(`  total:    ${total.toFixed(1)} s   (incl. create + verify + delete)`);
console.log(
  `  playback: HEAD ${head.status}, stored ${stored} bytes ${Number(stored) === buf.length ? "✓" : "✗"}`,
);
